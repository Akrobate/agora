Cypress.Commands.add('login', (email, password) => {
    cy.request({
        method: 'POST',
        url: 'http://192.168.1.11:8085/api/v1/users/login',
        body: {
            email,
            password,
        }
    })
    .then((login_response) => {
        window.localStorage.setItem('token', login_response.body.token);
    })
});

